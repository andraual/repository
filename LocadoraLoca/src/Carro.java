
public class Carro {

	public String fabricante;
	public String modelo;
	public int ano;
	public String cambio;
	public int id;	
	
	public Carro(String fabricante, String modelo, int ano, String cambio, int id) {
		
		this.fabricante = fabricante;
		this.modelo= modelo;
		this.ano= ano;
		this.cambio= cambio;
		this.id = id;
	}
	
	
	
	
	
	
}
